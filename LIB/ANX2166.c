static void TCON_WriteReg(uint8 osb, uint8 addr1, uint8 addr2, uint8 addr3, uint8 p){
uint8 buf[5];
  buf[0] = 0x00;
  buf[1] = osb;
  buf[2] = addr1;
  buf[3] = addr2;
  buf[4] = addr3;  
  I2C_Write(0x3E, buf, 5); 

  buf[0] = 0x00;
  buf[1] = p; 
  I2C_Write(0x44, buf, 2); 
  
}
 

