static void PowerOn(void){
    SetGPIO(PWR_VDD, HIGH);
    _delay_ms(200);    
	uint8 cfg = GetGPI(0);
    if(cfg == 0)
       SetPSR_120HZ(); 
    else
       SetPSR_24HZ();
    _delay_ms(100);
    DispPattern(0);
    TCON_WriteReg(0x00, 0x00, 0x01, 0x0a, 0x80);	
	_delay_ms(100);

    SetGPIO(GPO_VTP, HIGH);	
    bPwrOn = 1;
}

static void PowerOff(void){
    TCON_WriteReg(0x00, 0x00, 0x01, 0x0a, 0x00);
    SetGPIO(GPO_BIST, LOW);
    SetGPIO(GPO_VTP, LOW);
    _delay_ms(200);
    SetGPIO(PWR_VDD, LOW);
    bPwrOn = 0;
}

static void AgingFunc_A(unsigned char step){
    switch(step){
      case 0:        //W
            if(bPwrOn == 0)
               PowerOn();
            DispPattern(0);
            DelayL(4000);
            break;
      case 1:        //BK
            DispPattern(1);
            DelayL(2000);
            break;
      case 2:        //R
            DispPattern(2);
            DelayL(2000);
            break; 
      case 3:        //G
            DispPattern(3);
            DelayL(2000);
            break; 
      case 4:        //B
            DispPattern(4);
            DelayL(2000);
            break;
      case 5:        //CHIDO
            DispPattern(5);
            DelayL(2000);
            break;      
      default: break;      
    }
}

static void AgingFunc_B(unsigned char step){
    switch(step){
      case 0:        //W
            if(bPwrOn == 0)
               PowerOn();
            DispPattern(0);
            DelayL(3400);
            break;
      case 1:        //BK
            DispPattern(1);
            DelayL(2000);
            break;
      case 2:        //R
            DispPattern(2);
            DelayL(2000);
            break; 
      case 3:        //G
            DispPattern(3);
            DelayL(2000);
            break; 
      case 4:        //B
            DispPattern(4);
            DelayL(2000);
            break;
      case 5:        //CHIDO
            DispPattern(5);
            DelayL(2000);
            break;  
      case 6:        //PWROFF
            PowerOff();
            DelayL(4000); 
            break;
      default: break;      
    }
}

static void EndFlow(void){
    TCON_WriteReg(0x00, 0x00, 0x01, 0x0a, 0x00);
    SetGPIO(GPO_BIST, LOW);
    _delay_ms(200);
    SetGPIO(PWR_VDD, LOW);
    _delay_ms(1200);
    SetGPIO(GPO_VTP, LOW);
    bPwrOn = 0;     
}



